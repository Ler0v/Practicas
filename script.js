function hacer () {

    numero = prompt('Ingresar numero')

    while(numero != "ESC"){
        if (numero == 1){
            alert('Seleccionaste tomate')
            break;
        }else if (numero == 2){
            alert('Seleccionaste papa')
            break;
        }else if (numero == 3){
            alert('Seleccionaste carne')
            break;
        }else if (numero == 4){
            alert('Seleccionaste pollo')
            break;
        }

    }

}